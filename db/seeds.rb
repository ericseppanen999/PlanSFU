def create_course_with_error_handling(course_data)
  begin
    Course.create!(course_data)
  rescue ActiveRecord::RecordNotUnique => e
    existing_course = Course.find_by(dept: course_data[:dept], number: course_data[:number], term: course_data[:term], year: course_data[:year])
    if existing_course
      Rails.logger.warn("Deleting duplicate course: #{existing_course.dept} #{existing_course.number} to replace with new data.")
      existing_course.destroy
      retry
    end
  rescue ActiveRecord::RecordInvalid => e
    Rails.logger.warn("Skipping invalid course: #{course_data[:dept]} #{course_data[:number]} - #{e.message}")
  end
end

